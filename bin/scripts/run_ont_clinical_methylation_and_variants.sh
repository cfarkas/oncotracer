          -a "$CLINICAL_REGIONS_BED" \
          -b "$bm" \
          -wa -wb > "$inter"
      fi
    fi

    python3 - "$sample" "$inter" "$MIN_CPG_COVERAGE" "$MOD_CODES_ARG" "$METH_SUMMARY" "$METH_PROMOTER" "$sample_percpg" "$PER_CPG_ALL" <<'PY'
import csv
import statistics
import sys
from collections import defaultdict

sample, inter, mincov, mod_codes_arg, summary_path, promoter_path, sample_percpg, all_percpg = sys.argv[1:]
mincov = float(mincov)

keep_mods = set(x for x in mod_codes_arg.split(",") if x) if mod_codes_arg else None

groups = defaultdict(list)

with open(sample_percpg, "w", newline="") as sp, open(all_percpg, "a", newline="") as ap:
    sw = csv.writer(sp, delimiter="\t")
    aw = csv.writer(ap, delimiter="\t")

    header = [
        "sample","gene","region_type","region_id","region_chrom","region_start","region_end",
        "cpg_chrom","cpg_start","cpg_end","mod_code","valid_coverage","percent_modified"
    ]
    sw.writerow(header)

    with open(inter) as fh:
        for line in fh:
            if not line.strip():
                continue

            f = line.rstrip("\n").split("\t")

            # 6 fields from region BED + at least 11 bedMethyl columns.
            if len(f) < 17:
                continue

            r_chrom, r_start, r_end, region_id, region_type, gene = f[:6]
            b = f[6:]

            cpg_chrom, cpg_start, cpg_end = b[0], b[1], b[2]
            mod_code = b[3]

            if keep_mods is not None and mod_code not in keep_mods:
                continue

            try:
                valid_cov = float(b[9])
                percent = float(b[10])
            except Exception:
                continue

            if valid_cov < mincov:
                continue

            key = (gene, region_type, region_id, r_chrom, r_start, r_end)
            groups[key].append((percent, valid_cov))

            row = [
                sample,gene,region_type,region_id,r_chrom,r_start,r_end,
                cpg_chrom,cpg_start,cpg_end,mod_code,valid_cov,percent
            ]
            sw.writerow(row)
            aw.writerow(row)

def write_summary(path, promoter_only=False):
    with open(path, "a", newline="") as out:
        w = csv.writer(out, delimiter="\t")

        for key in sorted(groups):
            gene, region_type, region_id, chrom, start, end = key

            if promoter_only and region_type != "promoter":
                continue

            vals = groups[key]
            perc = [x[0] for x in vals]
            cov = [x[1] for x in vals]

            n = len(vals)
            mean_p = sum(perc) / n
            med_p = statistics.median(perc)
            total_cov = sum(cov)
            weighted_p = sum(p * c for p, c in vals) / total_cov if total_cov else 0
            mean_cov = total_cov / n

            w.writerow([
                sample, gene, region_type, region_id, chrom, start, end, n,
                round(mean_p, 4), round(weighted_p, 4), round(med_p, 4),
                round(mean_cov, 4), round(total_cov, 4)
            ])

write_summary(summary_path, promoter_only=False)
write_summary(promoter_path, promoter_only=True)
PY
  done

  python3 - "$METH_PROMOTER" "$OUTDIR/methylation/promoter_methylation_matrix.tsv" <<'PY'
import csv
import sys
from collections import defaultdict

promoter_tsv, matrix_out = sys.argv[1:]

samples = []
genes = []
data = {}

with open(promoter_tsv) as fh:
    reader = csv.DictReader(fh, delimiter="\t")
    for row in reader:
        sample = row["sample"]
        gene = row["gene"]
        val = row["weighted_percent_modified"]

        if sample not in samples:
            samples.append(sample)
        if gene not in genes:
            genes.append(gene)

        data[(gene, sample)] = val

genes = sorted(genes)

with open(matrix_out, "w", newline="") as out:
    w = csv.writer(out, delimiter="\t")
    w.writerow(["gene"] + samples)

    for gene in genes:
        w.writerow([gene] + [data.get((gene, sample), "NA") for sample in samples])
PY
fi

###############################################################################
# Variant calling with ClairS-TO
###############################################################################

if [[ "$SKIP_VARIANTS" != "true" ]]; then
  mkdir -p "$(dirname "$CLAIRSTO_SIF")"

  if [[ ! -s "$CLAIRSTO_SIF" ]]; then
    echo "Pulling ClairS-TO container:"
    echo "  $CLAIRSTO_SIF"
    "$SINGULARITY_BIN" pull "$CLAIRSTO_SIF" docker://hkubal/clairs-to:latest
  fi

  CLINVAR_VCF="$(readlink -m "$CLINVAR_VCF")"
  mkdir -p "$(dirname "$CLINVAR_VCF")"

  if [[ ! -s "$CLINVAR_VCF" ]]; then
    echo "Downloading ClinVar GRCh38:"
    echo "  $CLINVAR_VCF"
    wget -O "$CLINVAR_VCF" \
      "https://ftp.ncbi.nlm.nih.gov/pub/clinvar/vcf_GRCh38/clinvar.vcf.gz"
  fi

  if [[ -s "$CLINVAR_VCF" && ! -s "$CLINVAR_VCF.tbi" && ! -s "$CLINVAR_VCF.csi" ]]; then
    bcftools index -t "$CLINVAR_VCF" || true
  fi

  for idx in "${!SAMPLES[@]}"; do
    sample="${SAMPLES[$idx]}"
    bam="${MODBAMS[$idx]}"

    sample_out="$OUTDIR/variants/clairsto/$sample"
    mkdir -p "$sample_out"

    echo
    echo "============================================================================"
    echo "Variant calling: $sample"
    echo "BAM: $bam"
    echo "============================================================================"

    [[ -s "$bam.bai" || -s "$bam.csi" ]] || samtools index -@ "$THREADS" "$bam"

    if [[ "$FORCE" == "true" ]]; then
      rm -rf "$sample_out"
      mkdir -p "$sample_out"
    fi

    EXTRA_ARGS=()
    if [[ "$DISABLE_VERDICT" == "true" ]]; then
      EXTRA_ARGS+=(--disable_verdict)
    fi

    if [[ ! -s "$sample_out/snv.vcf.gz" && ! -s "$sample_out/indel.vcf.gz" ]]; then
      "$SINGULARITY_BIN" exec \
        "${SING_NV[@]}" \
        -B /media/server/STORAGE:/media/server/STORAGE \
        "$CLAIRSTO_SIF" \
        /opt/bin/run_clairs_to \
          --tumor_bam_fn "$bam" \
          --ref_fn "$REF" \
          --threads "$THREADS" \
          --platform "$PLATFORM" \
          --output_dir "$sample_out" \
          --bed_fn "$VARIANT_TARGETS_BED" \
          --sample_name "$sample" \
          --conda_prefix /opt/micromamba/envs/clairs-to \
          "${EXTRA_ARGS[@]}"
    else
      echo "Reusing existing ClairS-TO VCFs for $sample"
    fi

    echo "Computing target-region coverage..."
    bedtools coverage \
      -a "$VARIANT_TARGETS_ANNOT_BED" \
      -b "$bam" \
      -mean \
      > "$OUTDIR/coverage/${sample}.target_region_coverage.tsv"

    for kind in snv indel; do
      vcf="$sample_out/${kind}.vcf.gz"
      [[ -s "$vcf" ]] || continue

      [[ -s "$vcf.tbi" || -s "$vcf.csi" ]] || bcftools index -t "$vcf" || true

      annotated="$OUTDIR/variants/annotated_vcf/${sample}.${kind}.clinvar.vcf.gz"

      if [[ -s "$CLINVAR_VCF" ]]; then
        if bcftools annotate \
            -a "$CLINVAR_VCF" \
            -c INFO/CLNSIG,INFO/CLNDN,INFO/GENEINFO \
            "$vcf" \
            -Oz -o "$annotated" \
            > "$OUTDIR/logs/${sample}.${kind}.clinvar_annotate.stdout.log" \
            2> "$OUTDIR/logs/${sample}.${kind}.clinvar_annotate.stderr.log"; then
          bcftools index -t "$annotated" || true
        else
          echo "WARNING: ClinVar annotation failed for $sample $kind; copying unannotated VCF."
          cp "$vcf" "$annotated"
          cp "$vcf.tbi" "$annotated.tbi" 2>/dev/null || true
        fi
      else
        cp "$vcf" "$annotated"
        cp "$vcf.tbi" "$annotated.tbi" 2>/dev/null || true
      fi
    done
  done

  python3 - "$OUTDIR" "$VARIANT_TARGETS_ANNOT_BED" "${SAMPLES[@]}" <<'PY'
import gzip
import os
import sys
from collections import defaultdict

outdir = sys.argv[1]
target_bed = sys.argv[2]
samples = sys.argv[3:]

targets = defaultdict(list)

with open(target_bed) as fh:
    for line in fh:
        if not line.strip():
            continue
        f = line.rstrip("\n").split("\t")
        if len(f) < 6:
            continue
        chrom, start, end, region_id, region_type, gene = f[:6]
        targets[chrom].append((int(start), int(end), gene, region_type, region_id))

for chrom in targets:
    targets[chrom].sort()

def open_vcf(path):
    return gzip.open(path, "rt") if path.endswith(".gz") else open(path)

def parse_info(s):
    d = {}
    if s == ".":
        return d
    for item in s.split(";"):
        if "=" in item:
            k, v = item.split("=", 1)
            d[k] = v
        else:
            d[item] = True
    return d

def overlap_targets(chrom, pos1):
    pos0 = pos1 - 1
    hits = []
    for start, end, gene, region_type, region_id in targets.get(chrom, []):
        if end <= pos0:
            continue
        if start > pos0:
            break
        hits.append((gene, region_type, region_id))
    return hits

rows = []

for sample in samples:
    for kind in ["snv", "indel"]:
        vcf = os.path.join(outdir, "variants", "annotated_vcf", f"{sample}.{kind}.clinvar.vcf.gz")
        if not os.path.exists(vcf):
            continue

        with open_vcf(vcf) as fh:
            for line in fh:
                if not line.strip() or line.startswith("#"):
                    continue

                f = line.rstrip("\n").split("\t")
                if len(f) < 8:
                    continue

                chrom, pos, vid, ref, alt, qual, flt, info_s = f[:8]
                pos = int(pos)
                info = parse_info(info_s)

                hits = overlap_targets(chrom, pos)
                if not hits:
                    hits = [(".", ".", ".")]

                fmt = f[8] if len(f) > 8 else "."
                sample_fields = f[9] if len(f) > 9 else "."

                fmt_map = {}
                if fmt != "." and sample_fields != ".":
                    keys = fmt.split(":")
                    vals = sample_fields.split(":")
                    fmt_map = dict(zip(keys, vals))

                clnsig = info.get("CLNSIG", ".")
                clndn = info.get("CLNDN", ".")
                geneinfo = info.get("GENEINFO", ".")

                af = (
                    fmt_map.get("AF")
                    or fmt_map.get("VAF")
                    or info.get("AF")
                    or info.get("VAF")
                    or "."
                )

                for gene, region_type, region_id in hits:
                    rows.append({
                        "sample": sample,
                        "variant_type": kind,
                        "chrom": chrom,
                        "pos": pos,
                        "ref": ref,
                        "alt": alt,
                        "qual": qual,
                        "filter": flt,
                        "target_gene": gene,
                        "target_region_type": region_type,
                        "target_region_id": region_id,
                        "clinvar_CLNSIG": clnsig,
                        "clinvar_CLNDN": clndn,
                        "clinvar_GENEINFO": geneinfo,
                        "DP": fmt_map.get("DP", "."),
                        "AD": fmt_map.get("AD", "."),
                        "AF_or_VAF": af,
                        "FORMAT": fmt,
                        "sample_FORMAT_values": sample_fields,
                    })

fields = [
    "sample","variant_type","chrom","pos","ref","alt","qual","filter",
    "target_gene","target_region_type","target_region_id",
    "clinvar_CLNSIG","clinvar_CLNDN","clinvar_GENEINFO",
    "DP","AD","AF_or_VAF","FORMAT","sample_FORMAT_values"
]

report_all = os.path.join(outdir, "variants", "reports", "candidate_variants.tsv")
report_key = os.path.join(outdir, "variants", "reports", "candidate_clinically_relevant_variants.tsv")

with open(report_all, "w") as out:
    out.write("\t".join(fields) + "\n")
    for r in rows:
        out.write("\t".join(str(r.get(x, ".")) for x in fields) + "\n")

key_genes = {
    "TP53","BRAF","IDH1","IDH2","TERT","ATRX","PTEN","CDKN2A","CDKN2B",
    "EGFR","PDGFRA","NF1","FGFR1","FGFR2","FGFR3","PIK3CA","PIK3R1",
    "H3F3A","H3F3B","H3-3A","H3-3B","CIC","FUBP1","SMARCB1","SMARCA4",
    "BAP1","TSC1","TSC2","NF2","BCOR","NTRK1","NTRK2","NTRK3","KMT2A"
}

def relevant(r):
    sig = str(r["clinvar_CLNSIG"]).lower()
    gene = str(r["target_gene"]).upper()

    return (
        "pathogenic" in sig
        or "likely_pathogenic" in sig
        or gene in key_genes
    )

with open(report_key, "w") as out:
    out.write("\t".join(fields) + "\n")
    for r in rows:
        if relevant(r):
            out.write("\t".join(str(r.get(x, ".")) for x in fields) + "\n")

print(f"Wrote: {report_all}")
print(f"Wrote: {report_key}")
PY
fi

