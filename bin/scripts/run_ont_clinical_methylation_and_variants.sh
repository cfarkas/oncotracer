###############################################################################
# Combined ONT clinical-region methylation summary + targeted variant calling
#
# Inputs:
#   --bedmethyl sample1.bed.gz,sample2.bed.gz
#   --modbam    sample1.mod.sorted.bam,sample2.mod.sorted.bam
#   --samples   sample1,sample2
#   --outdir    output_folder
#
# Outputs:
#   methylation/
#     region_methylation_summary.tsv
#     promoter_methylation_summary.tsv
#     all_regions_per_cpg.tsv
#
#   variants/
#     clairsto/<sample>/snv.vcf.gz
#     clairsto/<sample>/indel.vcf.gz
#     annotated_vcf/
#     candidate_variants.tsv
#     candidate_clinically_relevant_variants.tsv
#
#   coverage/
#     <sample>.target_region_coverage.tsv
#
# Notes:
#   - Variant calling is exploratory for low-pass WGS.
#   - Absence of a variant is not a true negative unless local coverage is adequate.
#   - For clinical use, confirm relevant findings with validated assays.
###############################################################################

