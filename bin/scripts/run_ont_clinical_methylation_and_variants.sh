import gzip
import re
import sys
from collections import defaultdict

gtf, genes_file, clinical_bed, variant_bed, upstream, downstream = sys.argv[1:]
upstream = int(upstream)
downstream = int(downstream)

genes = [x.strip() for x in open(genes_file) if x.strip()]
want = set(genes)

gene_info = {}
exons = defaultdict(list)

def parse_attrs(s):
    d = {}
    for part in s.strip().split(";"):
        part = part.strip()
        if not part:
            continue
        m = re.match(r'(\S+)\s+"([^"]+)"', part)
        if m:
            d[m.group(1)] = m.group(2)
    return d

opener = gzip.open if gtf.endswith(".gz") else open

with opener(gtf, "rt") as fh:
    for line in fh:
        if line.startswith("#"):
            continue

        f = line.rstrip("\n").split("\t")
        if len(f) < 9:
            continue

        chrom, source, feature, start, end, score, strand, frame, attrs = f
        a = parse_attrs(attrs)
        gene = a.get("gene_name")

        if not gene or gene not in want:
            continue

        start = int(start)
        end = int(end)

        if feature == "gene":
            gene_info[gene] = (chrom, start, end, strand)

        if feature == "exon":
            exons[gene].append((chrom, start, end, strand))

missing = sorted(want - set(gene_info))
if missing:
    sys.stderr.write("WARNING: missing genes in GTF: " + ",".join(missing) + "\n")

clinical_rows = []
variant_rows = []

for gene in genes:
    if gene not in gene_info:
        continue

    chrom, start, end, strand = gene_info[gene]

    if strand == "-":
        tss = end
        p_start = max(0, tss - downstream - 1)
        p_end = tss + upstream
    else:
        tss = start
        p_start = max(0, tss - upstream - 1)
        p_end = tss + downstream

    promoter = (chrom, p_start, p_end, f"{gene}|promoter_{upstream}_{downstream}", "promoter", gene)
    clinical_rows.append(promoter)
    variant_rows.append(promoter)

    clinical_rows.append((chrom, max(0, start - 1), end, f"{gene}|gene_body", "gene_body", gene))

    for j, (c, s, e, st) in enumerate(exons[gene], start=1):
        exon = (c, max(0, s - 1), e, f"{gene}|exon_{j}", "exon", gene)
        clinical_rows.append(exon)
        variant_rows.append(exon)

clinical_rows.sort(key=lambda x: (x[0], x[1], x[2], x[3]))
variant_rows.sort(key=lambda x: (x[0], x[1], x[2], x[3]))

with open(clinical_bed, "w") as out:
    for r in clinical_rows:
        out.write("\t".join(map(str, r)) + "\n")

with open(variant_bed, "w") as out:
    for r in variant_rows:
        out.write("\t".join(map(str, r)) + "\n")

sys.stderr.write(f"Wrote {len(clinical_rows)} clinical methylation regions to {clinical_bed}\n")
sys.stderr.write(f"Wrote {len(variant_rows)} variant target regions to {variant_bed}\n")
PY
fi

[[ -s "$CLINICAL_REGIONS_BED" ]] || { echo "ERROR: clinical regions BED is empty" >&2; exit 1; }
