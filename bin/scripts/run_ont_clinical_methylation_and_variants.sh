  printf 'sample\tgene\tregion_type\tregion_id\tregion_chrom\tregion_start\tregion_end\tcpg_chrom\tcpg_start\tcpg_end\tmod_code\tvalid_coverage\tpercent_modified\n' > "$PER_CPG_ALL"

